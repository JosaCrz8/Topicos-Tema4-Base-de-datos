    private void TXTbuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TXTbuscarKeyPressed
       
        String sql = "SELECT * FROM cliente WHERE codigoC LIKE '%" + TXTbuscar.getText() + "%' "
        + "OR nombreC LIKE '%" + TXTbuscar.getText() + "%'";
   
        String [] titulos = {"#","Codigo", "Nombre", "Apellido", "Direccion", "Telefono", "Estado"};
        ResultSet rs = null;
        DefaultTableModel tabla = new DefaultTableModel(null, titulos);
        rs=Configuracion.conexion.consutaSQL(sql);
        int x=0;
                try
                {
                    int  nf =ControlCliente.getRowsCliente();
                    Object[][] filas = new String [nf][7];
                    while (rs.next()) {
                        filas[x][0] =(x+1) + "";
                        filas[x][1] = rs.getString("codigoC");
                        filas[x][2] = rs.getString("nombreC");
                        filas[x][3] = rs.getString("apellidoC");
                        filas[x][4] = rs.getString("direccion");
                        filas[x][5] = rs.getInt("telefono") + "";
                        filas[x][6] = rs.getString("estado");
                        x++;
                    }
                    tabla.setDataVector(filas, titulos);
                    jcliente.setModel(tabla);
                } catch (SQLException ex)
                {
                    System.out.println("ERROR AL BUSCAR LOS DATOS : " + ex.getMessage());
                    Logger.getLogger(Producto.class.getName()).log(Level.SEVERE, null, ex); 
                    }
    }//GEN-LAST:event_TXTbuscarKeyPressed


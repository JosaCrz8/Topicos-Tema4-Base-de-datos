 private void MostrarTabla(){
     String [] titulo = {"#","Codigo", "Nombre", "Apellido", "Direccion", "Telefono", "Estado"};
     DefaultTableModel tabla = new DefaultTableModel();
     ResultSet rs = ControlCliente.mostrarCliente();
     int x=0;
     try {
         int  nf =ControlCliente.getRowsCliente();
         Object[][] filas = new String [nf][7];
         while (rs.next()) {
           filas[x][0] =(x+1) + "";
           filas[x][1] = rs.getString("codigoC");
           filas[x][2] = rs.getString("nombreC");
           filas[x][3] = rs.getString("apellidoC");
           filas[x][4] = rs.getString("direccion");
           filas[x][5] = rs.getInt("telefono") + "";
           filas[x][6] = rs.getString("estado");
           x++;
         }
         tabla.setDataVector(filas, titulo);
         jcliente.setModel(tabla);
     } catch (SQLException ex) {
          Logger.getLogger(Cliente.class.getName()).log(Level.SEVERE, null, ex);
     }
 }


